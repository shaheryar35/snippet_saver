  @doc """
  Returns the list of permissions assigned to an employee.
  """
  def list_permissions_for_employee(employee_id) when is_integer(employee_id) do
    employee_permissions_query(employee_id)
    |> Repo.all()
  end

  @doc """
  Returns the list of permissions that are NOT assigned to an employee.
  """
  def list_available_permissions_for_employee(employee_id) when is_integer(employee_id) do
    available_permissions_query(employee_id)
    |> Repo.all()
  end

  @doc """
  Assigns a permission to an employee (no-op if it already exists).
  """
  def assign_permission_to_employee(employee_id, permission_id)
      when is_integer(employee_id) and is_integer(permission_id) do
    now = NaiveDateTime.utc_now() |> NaiveDateTime.truncate(:second)

    Repo.insert_all(
      "employee_permissions",
      [
        %{
          employee_id: employee_id,
          permission_id: permission_id,
          inserted_at: now,
          updated_at: now
        }
      ],
      on_conflict: :nothing,
      conflict_target: [:employee_id, :permission_id]
    )
  end

  @doc """
  Unassigns a permission from an employee.
  """
  def unassign_permission_from_employee(employee_id, permission_id)
      when is_integer(employee_id) and is_integer(permission_id) do
    from(ep in "employee_permissions",
      where: ep.employee_id == ^employee_id and ep.permission_id == ^permission_id
    )
    |> Repo.delete_all()
  end

  defp employee_permissions_query(employee_id) do
    from p in Permission,
      join: ep in "employee_permissions",
      on: ep.permission_id == p.id,
      where: ep.employee_id == ^employee_id,
      select: p
  end

  defp available_permissions_query(employee_id) do
    from p in Permission,
      where:
        p.id not in
          subquery(
            from ep in "employee_permissions",
              where: ep.employee_id == ^employee_id,
              select: ep.permission_id
          ),
      select: p
  end

